package gHybrid_Inheritance;

public class aInheritance {

	

/*
 		* Inheritance is a mechanism in which one class acquires the " 
 		  properties(i.e variables and methods) " of another class.
 		  
 		* Inheritance is mainly used for " Code Reusability "
 		
 		* Baseclass = Parent Class  extends classes are Child classes 
 		  eg : Parents money & property was extend to children.
 		
 		* Child class can have specific properties(i.e. variables and methods) which ae 
 		  not available in the parent class.
 		  
 		* Object created for parent class can access the variables and methods that are 
 		  created in parent class only. It can't access child class properties.
 		  
 		* Object created for child class which is inheriting the parent class can access
 		  the variables and methods of bothparent class and child class.
 		
 		'IS A' Relationship :
 			eg : Benz is a car
 			
 		Different types of Inheritance?
 			1. Single Inheritance
 			2. Multiple Inheritance
 			3. Multilevel Inheritance
 			4. Hierarchical Inheritance
 			5. Hybrid Inheritance
 			
 		* Small trick to find parent and child class
	  
		  class "before" extends is called child class (or) sub class.
		  class "after" extends is called parent class (or) super class.
 		
 */
		
		
	

}
